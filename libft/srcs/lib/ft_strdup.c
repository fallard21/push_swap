#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*str;
	int		len;

	len = ft_strlen(s);
	str = ft_memalloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[i];
		i++;
	}
	return (str);
}
